package com.torodb.core.backend;

import static java.lang.annotation.ElementType.METHOD;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Methods annotated with this annotation require to be called on a exclusive mode where there
 * are not other open transactions.
 *
 * <p>It is the caller responsability to ensure that there are no transaction open when this method
 * is called.
 */
@Retention(RetentionPolicy.SOURCE)
@Target({METHOD})
@Documented
public @interface ExclusiveDdl {

}
